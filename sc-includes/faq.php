        <span class="terms_title">
            ¿Cómo publicar un anuncio?
        </span>
        <p>Clica en publicar anuncio gratis, rellena el formulario. Recibirás una contraseña en tu correo en
            la bandeja de entrada, si no lo encuentras, busca en <strong>correo no deseados</strong> o en <strong>la carpeta
                otros</strong>.</p>
        <h2>¿Cuánto tiempo tarda para publicar mi anuncio?</h2>
        <p>
            Puede tardar un máximo de 24h (horario comercial).
        </p>
        <h2 >¿Como editar mi anuncio?</h2>
        <p >Clicando en el apartado "<strong>mi cuenta</strong>” insertando tu correo y contraseña. Si no te acuerdas de la
            contraseña, clica en "<strong>Recuperar contraseña</strong>".</p>



        <h2 >No veo mi anuncio publicado tampoco he recibido la contraseña.</h2>
        <p >Tu anuncio entrará en un cola y podría tardar un máximo de 24h
            (horario comercial) para ser aprobado.</p>
        <p >Todos los anuncios que no cumplan con las condiciones de uso
            recibirán un correo informando que no el anuncio no fue aprobado.</p>

        <h2 >No me acuerdo mi correo ni tampoco mi contraseña.</h2>
        <p >Entra en contacto con nosotros clicando en "<strong>contactar</strong>" y intentaremos ayudarte.</p>

        <h2 >¿Cuál es la diferencia del servicio Destacar anuncio y AutoSubidas?</h2>
        <p >Todos los pagos serán realizados a través de créditos. Podrás comprar
            créditos para todos los servicios destacados.</p>
        <p ><strong>Destacar anuncios</strong> se subirá el anuncio con su etiqueta a las primeras posiciones. Quedará fijo en una posición, y se irá colocando aleatoriamente con otros anuncios destacados. Aparecerá en todas las páginas.</p>
        <p ><strong>AutoSubidas</strong> se subirá el anuncio con su etiqueta a la primera
            posición, e irá bajando posiciones a medida que se publiquen
            nuevos anuncios. Podrás elegir subir cada 30 min o 1 hora.</p>

        <p ><strong>Subidas normales</strong> se subirá un anuncio sin etiqueta de forma manual.</p>

        <h2 >He publicado mi anuncio en una categoría y aparece en otra.</h2>
        <p >Debes publicar en la categoría que corresponda a tus servicios, sino, tu anuncio podría ser
            cambiado a la categoría correcta o ser eliminado.</p>
        <h2 >Ha desaparecido imágenes y textos de mi anuncio.</h2>
        <p >Nosotros intentamos adaptar tu anuncio a nuestras <strong>condiciones de
                uso</strong>.</p>

        <h2 >¿Puedo publicar/renovar anuncios repetidos?</h2>
        <p >Para mejorar la experiencia del visitante no está permitido publicar/renovar un anuncio que ya
            ha sido publicado/renovado recientemente, sino tu anuncio podría no ser publicado/renovado o
            eliminado.</p>
        <h2 >Soy un centro-publicista y publico/renuevo muchos anuncios a la vez.</h2>
        <p >Nosotros creamos un sistema con el que podrás renovar tus anuncios masivos (todos a las
            vez, no repetidos) y serán publicados con un intervalo de tiempo.</p>
        <p >Si eres un centro o publicista escríbenos para activar el sistema.</p>
        <h2 >¿Puedo publicar anuncios en varias categorías a la vez?</h2>
        <p >No, tendrás que elegir una sola categoría.</p>
        <h2 >¿Puedo incluir mi link/url de mi web en mi anuncio?</h2>
        <p >Esta opción ya no está permitida, solo esta permitido para los servicos de pago destacar
            banner.</p>
        <h2 >¿Puedo usar mis créditos de subir al listado para todo mis anuncios?</h2>
        <p >Sí, podrás usar los créditos para subir todos tus anuncios.</p>

        <h2 >¿Tengo que poner mi nombre para publicar un anuncio?</h2>
        <p >No necesita poner tu nombre verdadero, puedes poner la palabra masajista, quiromasajista etc.
            Puedes poner el nombre que quieras.</p>
        <h2 >¿Cuántos anuncios puedo publicar/renovar al día?</h2>
        <p >Puedes tener varios anuncios en tu cuenta pero hay un limite para renovar gratis.</p>

        <h2 >¿Hice un ingreso en efectivo/transferencia bancaria pero no veo mis servicios
            destacado?</h2>
        <p >Una vez hemos recibido el pago tu servicio será activado en un plazo
            máximo de 24 horas (horario comercial).</p>


        <h2 >¿He contratado un servicio destacar banner y he pago por tarjeta pero aún no lo veo
            publicado?</h2>
        <p >Todos los banner destacados pasan por una revisión. Una vez hemos
            recibido el pago y el banner aprobado, será publicado en un plazo
            máximo de 24h (horario laboral).</p>
        <p >Recordamos que no está permitido publicar imágenes en el servicio
            banner, solo formato banner en la posición horizontal con las medidas
            proporcionada por la web.</p>

